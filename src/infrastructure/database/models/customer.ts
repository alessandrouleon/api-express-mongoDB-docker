import mongoose, { Document, Schema } from 'mongoose';
import { Customer } from '../../../domain/entities/customers/customer.entity';

interface CustomerModel extends Customer, Document { }

const customerSchema = new Schema<CustomerModel>({
    name: {
        type: String,
        required: true,
        trim: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
        trim: true,
    },
    phone: {
        type: String,
        required: true,
        unique: true,
        trim: true,
    },
    address: {
        type: String,
        required: true,
        trim: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
    updatedAt: {
        type: Date,
        default: null,
    },
    deletedAt: {
        type: Date,
        default: null,
    }
});

export const custormerModel = mongoose.model<CustomerModel>('Customer', customerSchema);
